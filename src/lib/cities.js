// ── Arabic text normalisation ─────────────────────────────────────────────────
export function normalizeArabic(text) {
  if (!text) return "";
  return text
    .replace(/[أإآٱ]/g, "ا")
    .replace(/ة/g, "ه")
    .replace(/ى/g, "ي")
    .replace(/ؤ/g, "و")
    .replace(/ئ/g, "ي")
    .replace(/[ء]/g, "")
    .replace(/ـ/g, "")
    .replace(/[\u064B-\u065F\u0670]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

// ── Smart city matching ───────────────────────────────────────────────────────
export function cityMatches(candidate, query) {
  if (!candidate || !query) return !query;
  const normC = normalizeArabic(candidate);
  const normQ = normalizeArabic(query.trim());
  if (!normQ) return true;
  if (normC === normQ) return true;
  if (normC.includes(normQ)) return true;
  if (normQ.includes(normC)) return true;
  return false;
}
